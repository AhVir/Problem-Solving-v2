#include <iostream>
using namespace std;

int main() {
  cout << "I'm gone!!" << endl;
  cout << "Binary AND(&): " << endl;
  cout << "5 & 7 => " << (5 & 7) << endl;

  cout << "Binary OR(|): " << endl;
  cout << "5 | 8 => " << (5 | 8) << endl;

  cout << "XOR(Exclusive OR): " << endl;
  cout << "5 ^ 7 => " << (5 ^ 7) << endl;

  cout << "Binary NOT(~)" << endl;
  cout << "~0(as int, not bit) => " << ~0 << endl;
  cout << "~1(as int, not bit) => " << ~1 << endl;

  return 0;
}
